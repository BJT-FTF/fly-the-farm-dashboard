export function getRecipesForWeed(weed: string): TankMixRecipe[] {
  const w = weed.toLowerCase();
  return tankMixRecipes.filter((r) => r.weed.toLowerCase().includes(w));
}

export function getRecipeById(id: string): TankMixRecipe | undefined {
  return tankMixRecipes.find((r) => r.id === id);
}
